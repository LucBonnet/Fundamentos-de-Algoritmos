# Exercício 3:

# Escreva uma função com parâmetros que receba a base e a 
# altura de um triângulo e retorne sua área 
# (A = base * altura / 2).

def areaTriangulo(b, h):
  return (b * h) / 2

print(areaTriangulo(10, 2))
print(areaTriangulo(5, 6))
print(areaTriangulo(19, 7))
print(areaTriangulo(53, 27))